// common storage location for generic enums

namespace Nez
{
	public enum HorizontalAlign : byte
	{
		Left,
		Center,
		Right
	}


	public enum VerticalAlign : byte
	{
		Top,
		Center,
		Bottom
	}


	public enum Edge : byte
	{
		Top,
		Bottom,
		Left,
		Right
	}


	public enum Direction : byte
	{
		Up,
		Down,
		Left,
		Right
	}
}